package com.twinsoft.convertigo.eclipse.property_editors;

import org.eclipse.jface.viewers.ITableLabelProvider;
import org.eclipse.jface.viewers.LabelProvider;
import org.eclipse.swt.SWT;
import org.eclipse.swt.graphics.Image;
import org.eclipse.swt.widgets.Item;

public class ArrayEditorLabelProvider extends LabelProvider implements ITableLabelProvider {
	private int style = SWT.NONE;
	
	public ArrayEditorLabelProvider(int style) {
		this.style = style;
	}
	public Image getColumnImage(Object element, int columnIndex) {
		return null;
	}

	public String getColumnText(Object element, int columnIndex) {
		
		if (element instanceof Item) {
			element = ((Item) element).getData();
		}
		
		ArrayEditorRow row = (ArrayEditorRow) element;
		Object object = row.getValue(columnIndex);
		String text = object.toString();
		return (style & SWT.PASSWORD) == 0 ? text:text.replaceAll(".", "*");
	}
}
